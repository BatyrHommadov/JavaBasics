package Class8Loops;

public class Task1 {
    public static void main(String[] args) {
        /*
        Counting up to a given number
        Declare an integer variable count and initialize it to 1.
        Use a while loop to print out the value of count on each iteration, until count reaches a given number (e.g., 10).
        Increment count by 1 on each iteration
         */

        int count=1;
        while (count<=10){
            System.out.println(count);
            count++;
        }
    }
}
