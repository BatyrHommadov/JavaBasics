package Class9Arrays;

public class Task2WhileLoop {
    public static void main(String[] args) {

        //2) 2 4 6 8 10 12 14

        int [] numbers={2,4,6,8,10,12,14};

        int i=0;
        while(i<numbers.length){
            System.out.println(numbers[i]);
            i++;
        }
    }
}
