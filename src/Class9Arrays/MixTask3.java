package Class9Arrays;

public class MixTask3 {
    public static void main(String[] args) {
        // 3) 5 4 3 2 1
        //for (int i = 5; i => 1; i--)

        for (int i = 5; i > 0; i--) {
            System.out.print(i+" ");

        }

        System.out.println();
        int num=5;
        while(num>0){
            System.out.print(num+" ");
            num--;
        }
    }
}
