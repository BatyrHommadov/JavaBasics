package Class9Arrays;

public class MixTask5 {
    public static void main(String[] args) {
        //5) 3 6 9 12 15

        for (int i = 3; i <= 15; i++) {
            if (i % 3 == 0) {
                System.out.print(i + " ");
            }
        }
        System.out.println();
        int num = 3;
        while (num <= 15) {
            if (num % 3 == 0)
                System.out.print(num + " ");
                num++;

            }
        }
    }


