package Class9Arrays;

public class Task1ForLoop {
    public static void main(String[] args) {

        //1) 4 5 6 7 8 9 10 11 - while loop

        int [] numbers={4,5,6,7,8,9,10,11};

        for (int i = 0; i < numbers.length; i++) {
            System.out.println(numbers[i]);

        }
    }
}
